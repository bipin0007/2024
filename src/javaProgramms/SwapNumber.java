package javaProgramms;

public class SwapNumber {

	public static void main(String[] args) {
		// Swap the given 2 number
		//Ex: a = 20, b=50, so print a=50 and b=20
		
		int a = 10;
		
		int b = 30;
		
		int c = a;
		
		a=b;
		
		b=c;
		
		System.out.println("The Swapped Numbers are " + a + " " + b);

	}

}
